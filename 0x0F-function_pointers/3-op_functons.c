#include "3-calc.h"

/**
 * op_add - func return sum of a and b
 * @a: int input
 * @b: int input
 * Return: a + b
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - func returns sub a and b
 * @a: input int
 * @b: input int
 * Return: a - b
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - func returns multiplication of a and b
 * @a: input int
 * @b: input int
 * Return: a * b
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - func returns division of a and b
 * @a: input int
 * @b: input int
 * Return: a / b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - func returns module a by b
 * @a: input int
 * @b: input int
 * Return: a % b
*/

int op_mod(int a, int b)
{
	return (a % b);
}
